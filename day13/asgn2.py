a = 4
b = 3
if a < b:
    print(f"{a} is less than {b}")
else:
    print(f"{a} is greater than {b}")
if a <= b:
    print(f"{a} is less = than {b}")
else:
    print(f"{a} is greater = than {b}")
if a == b:
    print(f"{a} is equal to {b}")
else:
    print(f"{a} is not equal to {b}")